class trapezoid
{
    public static void main(String args[]) 
    {
        // Predefined values for the trapezoid sides
        double sideA = 5.0;
        double sideB = 8.0;
        double sideC = 6.0;
        double sideD = 10.0;

        
        double perimeter = sideA + sideB + sideC + sideD;

        // Print the result
        System.out.println("The perimeter of the trapezoid is: " + perimeter);
    }
}